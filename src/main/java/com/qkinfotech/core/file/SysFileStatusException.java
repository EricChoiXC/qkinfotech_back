package com.qkinfotech.core.file;

public class SysFileStatusException  extends Exception {

	public SysFileStatusException() {
		super();
	}

	public SysFileStatusException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public SysFileStatusException(String message, Throwable cause) {
		super(message, cause);
	}

	public SysFileStatusException(String message) {
		super(message);
	}

	public SysFileStatusException(Throwable cause) {
		super(cause);
	}

}
